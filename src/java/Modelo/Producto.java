package Modelo;

/**
 *
 * @author diegoeekhout
 */
public class Producto {
    int id;
    String cod;
    String nom;

    public Producto() {
    }

    public Producto(String cod, String nom) {
        this.cod = cod;
        this.nom = nom;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCod() {
        return cod;
    }

    public void setCod(String cod) {
        this.cod = cod;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }
    
    
}
